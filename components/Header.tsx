// --- Notification Center Component ---

interface NotificationCenterProps {
    notifications: Notification[];
    markNotificationRead: (id: string) => void;
    setActiveView: (view: View) => void;
    unreadCount: number;
}

const NotificationCenter: React.FC<NotificationCenterProps> = ({ notifications, markNotificationRead, setActiveView, unreadCount }) => {
    const [isOpen, setIsOpen] = useState(false);
    const notificationsRef = useRef<HTMLDivElement>(null);

    // Effect for closing dropdown when clicking outside
    useEffect(() => {
        const handleClickOutside = (event: MouseEvent) => {
            if (notificationsRef.current && !notificationsRef.current.contains(event.target as Node)) {
                setIsOpen(false);
            }
        };
        document.addEventListener("mousedown", handleClickOutside);
        return () => {
            document.removeEventListener("mousedown", handleClickOutside);
        };
    }, []);

    const handleNotificationClick = useCallback((notification: Notification) => {
        if (notification.view) {
            setActiveView(notification.view);
        }
        markNotificationRead(notification.id);
        // Keep open briefly to see the change, then close after a short delay
        setTimeout(() => setIsOpen(false), 200);
    }, [markNotificationRead, setActiveView]);

    const sortedNotifications = [...notifications].sort((a, b) => new Date(b.timestamp).getTime() - new Date(a.timestamp).getTime());

    return (
        <div className="relative" ref={notificationsRef}>
            <button
                onClick={() => setIsOpen(prev => !prev)}
                className="p-2 text-gray-400 hover:text-white focus:outline-none transition duration-200 relative"
                aria-label="Open Notifications"
            >
                <svg className="h-6 w-6" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M15 17h5l-1.405-1.405A2.032 2.032 0 0118 14.158V11a6.002 6.002 0 00-4-5.659V5a2 2 0 10-4 0v.341C7.67 6.165 6 8.388 6 11v3.159c0 .538-.214 1.055-.595 1.436L4 17h5m6 0v1a3 3 0 11-6 0v-1m6 0H9" />
                </svg>
                {unreadCount > 0 && (
                    <span className="absolute top-0 right-0 block h-3 w-3 rounded-full bg-red-500 ring-2 ring-gray-900 text-xs text-white flex items-center justify-center p-1 leading-none">
                        {unreadCount > 9 ? '9+' : unreadCount}
                    </span>
                )}
            </button>
            {isOpen && (
                <div className="absolute right-0 mt-3 w-[320px] sm:w-[400px] bg-gray-850 border border-red-600/30 rounded-xl shadow-2xl z-50 overflow-hidden transform origin-top-right scale-100 transition-transform duration-200">
                    <div className="p-4 font-bold text-lg text-white bg-gray-800 border-b border-red-600/30 flex justify-between items-center">
                        <span>System Alerts ({unreadCount} Critical)</span>
                        <button onClick={() => setIsOpen(false)} className="text-gray-400 hover:text-white text-xl leading-none">&times;</button>
                    </div>
                    <div className="max-h-[60vh] overflow-y-auto custom-scrollbar">
                        {sortedNotifications.length === 0 ? (
                            <p className="p-4 text-sm text-gray-400 italic">No pending system alerts. Operational apathy confirmed.</p>
                        ) : (
                            sortedNotifications.map(notification => (
                                <div
                                    key={notification.id}
                                    onClick={() => handleNotificationClick(notification)}
                                    className={`p-3 text-sm flex items-start transition duration-150 cursor-pointer hover:bg-gray-700/50 ${notification.read ? 'opacity-70' : 'bg-red-900/20 border-l-4 border-red-500'}`}
                                >
                                    {!notification.read && (
                                        <div className="w-2 h-2 rounded-full bg-red-400 mt-1.5 mr-2 flex-shrink-0 animate-ping-slow"></div>
                                    )}
                                    <div className="flex-grow">
                                        <p className={`text-gray-100 ${notification.read ? 'font-normal' : 'font-semibold'}`}>{notification.message}</p>
                                        <p className="text-xs text-gray-400 mt-1 flex justify-between items-center">
                                            <span>{notification.timestamp}</span>
                                            {!notification.read && <span className="text-red-400 text-[10px] uppercase font-bold">New</span>}
                                        </p>
                                    </div>
                                </div>
                            ))
                        )}
                    </div>
                    <div className="p-2 bg-gray-800 border-t border-red-600/30">
                        <button
                            onClick={() => { notifications.forEach(n => markNotificationRead(n.id)); setIsOpen(false); }}
                            className="w-full text-center text-xs text-red-400 hover:text-red-300 py-1 transition"
                        >
                            Ignore All System Events
                        </button>
                    </div>
                </div>
            )}
        </div>
    );
};


